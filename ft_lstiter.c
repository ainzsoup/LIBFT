#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*temp;

	while (lst != NULL)
	{
		f(lst->content);
		temp = lst->next;
		lst = temp;
	}
	lst = NULL;
}
